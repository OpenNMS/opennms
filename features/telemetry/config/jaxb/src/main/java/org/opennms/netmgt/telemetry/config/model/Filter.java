package org.opennms.netmgt.telemetry.config.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlValue;
import java.util.Objects;

/**
 * A rule which addresses belonging to this package must pass.
 * This package is applied only to addresses that pass this filter.
 */
@XmlRootElement(name="filter")
@XmlAccessorType(XmlAccessType.NONE)
public class Filter {
    @XmlValue
    private String content;

    public Filter() {
        // pass
    }

    public Filter(String content) {
        this.content = content;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Filter filter = (Filter) o;
        return Objects.equals(content, filter.content);
    }

    @Override
    public int hashCode() {
        return Objects.hash(content);
    }

    @Override
    public String toString() {
        return "Filter{" +
                "content='" + content + '\'' +
                '}';
    }
}
