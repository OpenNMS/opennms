package org.opennms.web.rest.v2.bsm.model.edge;

import java.util.Objects;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;


@XmlRootElement(name = "application-edge")
@XmlAccessorType(XmlAccessType.NONE)
public class ApplicationEdgeResponseDTO extends AbstractEdgeResponseDTO {

    @XmlElement(name = "application")
    private ApplicationResponseDTO application;

    public ApplicationResponseDTO getApplication() {
        return application;
    }

    public void setApplication(ApplicationResponseDTO application) {
        this.application = application;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        if (!super.equals(obj)) {
            return false;
        }
        // compare subclass fields
        return Objects.equals(application, ((ApplicationEdgeResponseDTO) obj).application);
    }

    @Override
    public int hashCode() {
        return super.hashCode() + Objects.hash(application);
    }
}
